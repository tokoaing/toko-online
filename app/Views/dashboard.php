<script>
    function tampilBaner() {
        $.ajax({
            url: "<?= base_url() ?>/home/tampilBaner",
            dataType: "json",
            success: function(response) {
                if (response.data) {
                    $('.tampilBaner').html(response.data);
                }
            },
            error: function(xhr, ajaxOptions, thrownError) {
                alert(xhr.status + '\n' + thrownError);
            }
        });
    }

    $(document).ready(function() {
        tampilBaner();
    });
</script>


